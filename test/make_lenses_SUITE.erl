test_make_lenses() -> 
    [].

%%--------------------------------------------------------------------
%% @spec TestCase(Config0) ->
%%               ok | exit() | {skip,Reason} | {comment,Comment} |
%%               {save_config,Config1} | {skip_and_save,Reason,Config1}
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% Comment = term()
%% @end
%%--------------------------------------------------------------------
test_make_lenses(_Config) -> 
    Hello = test_state:hello(),
    World = test_state:world(),
    State = #state{hello = hello, world = world},
    ?assertEqual(hello, getter:view(Hello, State)),
    ?assertEqual(world, getter:view(World, State)),
    ?assertEqual(#state{hello = new_hello, world = world}, setter:set(Hello, new_hello, State)),
    ?assertEqual(#state{hello = hello, world = new_world}, setter:set(World, new_world, State)),
    ok.

