-module(setter).

%% API
-export([over/3, set/3]).

%%%===================================================================
%%% API
%%%===================================================================
over(Setter, AB, S) ->
    AFB = fun(A) -> identity:identity(AB(A)) end,
    identity:run((Setter(AFB))(S)).

set(Setter, B, S) ->
    over(Setter, function_instance:const(B), S).

